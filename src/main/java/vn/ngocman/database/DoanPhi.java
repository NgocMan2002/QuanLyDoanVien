package vn.ngocman.database;
public class DoanPhi {
	private String maDoanVien;
	private String maPhieu;
	private String ngayNop;
	private String liDoNop;
	private String soTien;
	public String getMaDoanVien() {
		return maDoanVien;
	}
	public void setMaDoanVien(String maDoanVien) {
		this.maDoanVien = maDoanVien;
	}
	public String getMaPhieu() {
		return maPhieu;
	}
	public void setMaPhieu(String maPhieu) {
		this.maPhieu = maPhieu;
	}
	public String getNgayNop() {
		return ngayNop;
	}
	public void setNgayNop(String ngayNop) {
		this.ngayNop = ngayNop;
	}
	public String getLiDoNop() {
		return liDoNop;
	}
	public void setLiDoNop(String liDoNop) {
		this.liDoNop = liDoNop;
	}
	public String getSoTien() {
		return soTien;
	}
	public void setSoTuen(String soTien) {
		this.soTien = soTien;
	}
	
}

